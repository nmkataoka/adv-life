export function consoleLog(...args: any[]): void {
  // eslint-disable-next-line no-console
  console.log(...args);
}

export function consoleWarn(...args: any[]): void {
  // eslint-disable-next-line no-console
  console.warn(...args);
}

export function consoleError(...args: any[]): void {
  // eslint-disable-next-line no-console
  console.error(...args);
}
